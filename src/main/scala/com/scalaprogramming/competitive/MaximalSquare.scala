    if (matrix.isEmpty) return 0

    val dp = Array.ofDim[Int](matrix.length + 1, matrix.head.length + 1)

    val edges = for {
      row <- 1 to matrix.length
      col <- 1 to matrix.head.length
      if (matrix(row - 1)(col - 1) == '1')
    } yield {
      dp(row)(col) = math.min(math.min(dp(row)(col - 1), dp(row - 1)(col)), dp(row - 1)(col - 1)) + 1
      dp(row)(col)
    }

    val edge = if (!edges.isEmpty) edges.max else 0

    //println("Edge * Edge => " + edge * edge)
    edge * edge
  }


